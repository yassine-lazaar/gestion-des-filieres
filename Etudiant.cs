using System;

public class Etudiant
{
	public Etudiant(string nom , string prenom , string cne, double note)
	{
		this.Etnom = nom;
		this.Etprenom = prenom;
		this.Etcne = cne;
		this.Etnote = note;		
	}
   
	
	public string Etnom;
	public string Etprenom;
	public string Etcne;
	public double Etnote;



}